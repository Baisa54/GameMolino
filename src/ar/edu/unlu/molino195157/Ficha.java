package ar.edu.unlu.molino195157;

public class Ficha
{
    //-------------------------------------------------------------------------------------
    // Atributos
    //-------------------------------------------------------------------------------------

    private Jugador jugador;
    private boolean eliminada;

    //-------------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------------

    public Ficha(Jugador jugador)
    {
        this.jugador = jugador;
        this.eliminada = false;
    }

    //-------------------------------------------------------------------------------------
    // Getters y Setters
    //-------------------------------------------------------------------------------------

    //-------------------------------------------------------------------------------------
    // Metodos
    //-------------------------------------------------------------------------------------

    public void eliminarFicha()
    {
        this.eliminada = true;
    }
}
